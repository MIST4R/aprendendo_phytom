#operaçaões com variaveis int
n1 = 5
n2 = 5

#somando os números
print("soma:")
print(n1 + n2)

#dividindo os números
print("subtração")
print(n1 - n2)

#mutiplicando os números
print("mutiplicação:")
print(n1 * n2)

#dividindo os números
print("divisão:")
print(n1 / n2)

#o mood dos números
print("mood:")
print(n1 % n2)

#o expoente dos números
print("expoente:")
print(n1 ** n2)

#o arredondamento da divisão dos números
print("arredondamennto:")
print(n1 // n2)

